// 【题目描述】
// 在一个 n*n 的中国象棋棋盘上，有 k
// 个不会移动位置的敌方“将”，每个“将”都看管着其自身周围邻接格领地。你所控制的“帅”降临在棋盘的第 x 行，第 y
// 列的出生点上（保证为不被看管的空格），并可以按照中国象棋的规则，每回合向“上下左右”四个方向中的任意一格移动一步，不可以走出棋盘，也不能走到“将”所在的位置。
// 初始你的“帅”有 h 点的血量，每回合结束时如果你停留在被至少一个“将”所看管的格子，你的“帅”会减少 1
// 点的血量，如果回合结束并完成扣血后你的“帅”血量不为 0，那么“帅”所处的格子就会被你占领。当血量降为 0 时本局终止。
// 你可以在出生点无限次重生你的“帅”并尝试不同的行动方案，在任何一次尝试中只要你能占领某个格子，就将其纳入你的领地版图，请回答你的版图中一共可以包括多少个格子（出生点必定在你的领地版图中）。
// 【输入描述】
// 输入第一行包括五个正整数 n, k, x, y, h (x, y <=
// n)，用空格隔开，分别代表地图的尺寸、将的个数、出生点的行号和列号，以及初始的血量。 从第二行开始连续 k
// 行，每行两个正整数 a, b，用空格隔开，表示每个将所处位置的行号和列号，输入保证没有两个将出现在同一位置。
// 对于第一个测试点，2 <= n <= 10，k = 1，h = 1
// 对于第二个测试点，2 <= n <= 100，k <= 100，h = 1
// 对于后两个测试点，2 <= n <= 1000，k <= 100000，h <= 5

// 【输出描述】
// 输出一个整数，表示领地的格子数。
// 【样例 1】
// 输入：
// 4 1 1 1 1
// 3 3
// 输出：
// 10
// 样例 1 字符画示意图：
// [S] [ ] [ ] [ ]
// [ ] [ ] [x] [ ]
// [ ] [x] [#] [x]
// [ ] [ ] [x] [-]
// 图例说明：
// [S]：帅的出生点 (Start)，计算在版图内。
// [ ]：未被控制的安全空格，可以安全抵达并占领。
// [#]：敌方“将”所在位置，不可进入。
// [x]：被“将”控制的区域。因初始血量 h=1，进入后扣血降为 0，无法占领。
// [-]：因被控制区完全封锁，无法在任何情况下安全抵达的区域。
// 提示：如图所示，帅无法安全抵达右下角的方格 [-] 和所有“将”的控制区域 [x]，故答案为 10（即所有 [S] 和 [ ]
// 的数量总和）。

// 【样例 2】
// 输入：
// 5 2 1 1 2
// 2 2
// 2 4
// 输出：
// 17
// 样例 2 字符画示意图：
// [S] [O] [ ] [x] [-]
// [O] [#] [x] [#] [x]
// [ ] [x] [ ] [x] [ ]
// [ ] [ ] [ ] [ ] [ ]
// [ ] [ ] [ ] [ ] [ ]
// 图例说明：
// [S]：帅的出生点 (Start)，计算在版图内。
// [ ]：未被控制的安全空格，可以安全抵达并占领。
// [O]：被“将”控制但成功占领的区域 (Occupied)（进入时血量为 2，扣除 1 点后仍有 1 点，符合占领条件）。
// [#]：敌方“将”所在位置，不可进入。
// [x]：被“将”控制且无法占领的区域（如果在此耗尽最后 1 点血量则无法占领）。
// [-]：因血量不足或路径限制而无法占领的空白格子。
// 提示：如图所示，帅可以在血量为 0 前抵达所有标为 [S]、[ ]、[O] 的区域，一共 17 个格子，故答案为 17。

// 用 max_blood[row][col] 记录到达每格的最高血量，初始化为 0
// 从 (x, y, h) 出发搜索
// 每次移动到相邻格时：
// 超出棋盘边界：跳过
// 是将的位置：跳过
// 到达新格的血量 ≤ max_blood[row][col]：跳过（剪枝）
// 否则：更新 max_blood，继续搜索
// 血量计算：到达受控格则 -1，到达安全格不变
// 扣血后血量为 0：不能占领此格，也不能继续从此格出发搜索
// 最后统计所有 max_blood[row][col] > 0 且满足占领条件的格子数。

#include <bits/stdc++.h>
using namespace std;

int maze[1002][1002] = {0};
int max_blood[1002][1002] = {0};
int a_move[4] = {0, 0, 1, -1};
int b_move[4] = {-1, 1, 0, 0};

void fun(int x, int y, int h)
{
    if (h <= max_blood[x][y])
        return;
    else
    {
        max_blood[x][y] = h;
        for (int i = 0; i < 4; i++)
        {
            int x_corrent = x + a_move[i];
            int y_corrent = y + b_move[i];
            if (maze[x_corrent][y_corrent] == 2)
                continue;
            else if (maze[x_corrent][y_corrent] == 1)
            {
                if (h <= 1)
                    continue;
                else
                    fun(x_corrent, y_corrent, h - 1);
            }
            else
                fun(x_corrent, y_corrent, h);
        }
    }
}

int main()
{
    // 初始化地图
    int n, k, x, y, h;
    cin >> n >> k >> x >> y >> h;
    // 图中用0表示安全格，1表示被控制格，2表示将领所在格
    // 给地图包一圈边并用2标记做围墙
    for (int j = 0; j <= n + 1; j++)
    {
        maze[0][j] = 2;
        maze[n + 1][j] = 2;
    }
    for (int i = 0; i <= n + 1; i++)
    {
        maze[i][0] = 2;
        maze[i][n + 1] = 2;
    }
    int a, b;
    int a_corrent, b_corrent;
    // 输入k个将领的位置
    for (int i = 0; i < k; i++)
    {
        cin >> a >> b;
        maze[a][b] = 2;
        for (int j = 0; j < 4; j++)
        {
            a_corrent = a + a_move[j];
            b_corrent = b + b_move[j];
            if (maze[a_corrent][b_corrent] != 2) // 不能覆写将领格和围墙
                maze[a_corrent][b_corrent] = 1;
        }
    }

    // 主搜索程序
    fun(x, y, h);
    int ans = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= n; j++)
        {
            if (max_blood[i][j] > 0)
                ans++;
        }
    }
    cout << ans;
}
