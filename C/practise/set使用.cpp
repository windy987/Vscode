#include <iostream>
#include <set>
using namespace std;

int main()
{
    set<int> s;
    int Q, op, x;
    cin >> Q;
    for (int i = 0; i < Q; i++)
    {
        cin >> op >> x;
        switch (op)
        {
        case 1:
            s.insert(x);
            break;
        case 2:
            s.erase(x);
            break;
        case 3:
            auto t = s.lower_bound(x);
            if (t == s.end())
                cout << -1 << endl;
            else
                cout << *t << endl;
            break;
        }
    }
}