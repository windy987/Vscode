/*
📌 题目 4：打印九九乘法表

描述
输出标准的 9×9 乘法表（左上三角即可）。

示例（部分输出）

1×1=1
1×2=2  2×2=4
1×3=3  2×3=6  3×3=9
...


考点

嵌套循环

格式化输出

控制打印格式
*/

#include <stdio.h>
int main()
{
    int i, j;
    for (j = 1; j <= 9; j++)
    {
        for (i = 1; i <= j; i++)
        {
            printf("%d*%d=%d\t", i, j, i * j);
            if (i == j)
                printf("\n");
        }
    }
}
